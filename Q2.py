#Taking Input
m=int(input('Enter your marks: '))
#Returning Output based on Input
if m>80:
    print('\nYour Grade is A')
elif m>=60:
    print('\nYour Grade is B')
elif m>=50:
    print('\nYour Grade is C')
elif m>=45:
    print('\nYour Grade is D')
elif m>=25:
    print('\nYour Grade is E')
else:
    print('\nYour Grade is F')